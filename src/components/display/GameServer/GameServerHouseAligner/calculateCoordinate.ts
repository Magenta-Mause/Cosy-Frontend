function calculateCoordinate(index: number) {
  const base = POSITIONS[index % POSITIONS.length];

  if (base === undefined) {
    return { x: 0, y: 0 };
  }

  return {
    x: base.x,
    y: base.y,
  };
}

export default calculateCoordinate;
